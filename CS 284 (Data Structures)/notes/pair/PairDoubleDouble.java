package notes.pair;

public class PairDoubleDouble {

    private double x;
    private double y;

    PairDoubleDouble(double base, double height) {
        x = base;
        y = height;
    }

    /**
     * @return double return the x
     */
    public double getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(double x) {
        this.x = x;
    }

    /**
     * @return double return the y
     */
    public double getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(double y) {
        this.y = y;
    }

}